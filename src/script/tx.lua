--x,y为拼音显示的坐标
function Shurufa(x, y)
    local pinyin = ""
    local word = ""
    while 1 do
        local tmp = nil
        pinyin, tmp = GetPinyin(x, y, word)
        if tmp ~= nil then
            word = tmp
        end
        if pinyin == "finish" then
            break;
        elseif pinyin ~= -1 and pinyin ~= "" then           
            word = word .. (SeleteHanzi(x, y, word, pinyin) or "")
        end
    end
    return word
end
  
